def _load_conf_value(key: str) -> str | None:
    for conf in (
        ROOT / "sarca.conf",
        Path.home() / ".local/share/sarca/sarca.conf",
    ):
        if not conf.is_file():
            continue
        for line in conf.read_text(encoding="utf-8").splitlines():
            line = line.strip()
            if not line or line.startswith("#") or "=" not in line:
                continue
            k, v = line.split("=", 1)
            if k.strip() == key:
                return v.strip().strip('"').strip("'")
    return None


