def _upload(
    client: httpx.Client,
    headers: dict[str, str],
    storage_id: str,
    filename: str,
    content: bytes,
    content_type: str,
    parent: str = "",
) -> None:
    files = {"file": (filename, io.BytesIO(content), content_type)}
    data = {"path": parent}
    r = client.post(
        f"/api/storages/{storage_id}/files/upload",
        headers=headers,
        files=files,
        data=data,
    )
    assert r.status_code == 201, f"upload {filename} failed: {r.status_code} {r.text}"


def _tree_names(
    client: httpx.Client, headers: dict[str, str], storage_id: str, path: str = ""
) -> dict[str, bool]:
    suffix = path.strip("/")
    url = f"/api/storages/{storage_id}/files/tree/{suffix}" if suffix else f"/api/storages/{storage_id}/files/tree"
    # API path is /*path under files — tree is GET .../files/tree or .../files/tree/{path}
    r = client.get(url, headers=headers)
    if r.status_code == 404:
        r = client.get(f"/api/storages/{storage_id}/files/tree/", headers=headers)
    assert r.status_code == 200, r.text
    items = r.json()
    if isinstance(items, dict):
        items = items.get("files") or items.get("fs") or items.get("items") or []
    return {i["name"]: bool(i.get("is_file", True)) for i in items}


def test_ui_serves_new_bundle(client: httpx.Client) -> None:
    r = client.get("/")
    assert r.status_code == 200
    html = r.text
    assert "index-" in html or "/assets/" in html
    # hashed css from current vite build should be referenced
    assert ".css" in html
    # theme toggle / file icons live in the JS bundle — fetch first asset script
    import re

    m = re.search(r'/assets/(index-[^"]+\.js)', html)
    assert m, html[:500]
    js = client.get(f"/assets/{m.group(1)}")
    assert js.status_code == 200
    body = js.text
    assert "file-type-icon" in body or "FileTypeIcon" in body or "toggleThemeMode" in body or "data-theme" in body


def test_upload_image_and_video_smoke(
    client: httpx.Client, auth_headers: dict[str, str], storage_id: str
) -> None:
    png = (FIXTURES / "smoke.png").read_bytes() if (FIXTURES / "smoke.png").is_file() else _minimal_png()
    mp4 = (FIXTURES / "smoke.mp4").read_bytes() if (FIXTURES / "smoke.mp4").is_file() else _minimal_mp4()
    stamp = str(int(time.time()))
    img_name = f"smoke-{stamp}.png"
    vid_name = f"smoke-{stamp}.mp4"

    _upload(client, auth_headers, storage_id, img_name, png, "image/png")
    _upload(client, auth_headers, storage_id, vid_name, mp4, "video/mp4")

    # Trailing slash on parent must still produce a file path (not a folder marker).
    nested = f"nested-{stamp}.png"
    _upload(
        client,
        auth_headers,
        storage_id,
        nested,
        png,
        "image/png",
        parent="smoke-dir/",
    )

    r = client.get(f"/api/storages/{storage_id}/files/tree/", headers=auth_headers)
    assert r.status_code == 200, r.text
    names = {e["name"]: e["is_file"] for e in r.json()}
    assert names.get(img_name) is True, names
    assert names.get(vid_name) is True, names
    assert names.get("smoke-dir") is False, names

    r = client.get(
        f"/api/storages/{storage_id}/files/tree/smoke-dir", headers=auth_headers
    )
    assert r.status_code == 200, r.text
    nested_names = {e["name"]: e["is_file"] for e in r.json()}
    assert nested_names.get(nested) is True, nested_names
